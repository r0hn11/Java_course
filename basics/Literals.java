package com.complete.basics;

public class Literals {
    public static void main(String[] args){

        // Literal: A constant value that is assigned to a variable.

        byte n1 = 103;    // 103 is integer literal (byte,int,short)
        long n2 = 3579555187L;    // long literal (L)
        float n3 = 2.97f;    // 2.97f float literal (f or F)
        double n4 = 2.972573456d;    // double literal (d or D or none)
        char c1 ='A';   // 'A' is character literal
        String s1 = "Program";    // string literal
        boolean b1 = true;    // boolean literal

    }
}
